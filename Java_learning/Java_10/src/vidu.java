
public class vidu {
	public static void main(String[] args) {
		int a  = 100;
		int b = 2;
		
		System.out.println("Gia tri a = " + a);
		System.out.println("Gia tri b = " + b);
		
		// Ep kieu ngam dinh
		float c = a;
		float d = b;
		System.out.println("Gia tri c = "+ c);
		System.out.println("Gia tri d = " + d);
		
		System.out.println("-------------------------");
		
		// ep kieu tuong minh
		float e = 3.51f;
		float f = 9.53f;
		System.out.println("Gia tri e = "+ e);
		System.out.println("Gia tri f = " +f);
		
		int g = (int)e;
		int h = (int)f;
		System.out.println("Gia tri g = " + g);
		System.out.println("Gia tri h = " + h);
		
		//Ep kieu giua bien nguyen thuy va doi tuong
		int x = new Integer(324);
		System.out.println("X = " + x); 
		
		
	}

}
