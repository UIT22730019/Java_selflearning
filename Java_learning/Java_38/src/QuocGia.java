
public class QuocGia {
	private String thongTinMaQuocgia, tenQuocgia;

	public QuocGia(String thongTinMaQuocgia, String tenQuocgia) {
		this.thongTinMaQuocgia = thongTinMaQuocgia;
		this.tenQuocgia = tenQuocgia;
	}

	public String getThongTinMaQuocgia() {
		return thongTinMaQuocgia;
	}

	public void setThongTinMaQuocgia(String thongTinMaQuocgia) {
		this.thongTinMaQuocgia = thongTinMaQuocgia;
	}

	public String getTenQuocgia() {
		return tenQuocgia;
	}

	public void setTenQuocgia(String tenQuocgia) {
		this.tenQuocgia = tenQuocgia;
	}
	
	
	

}
